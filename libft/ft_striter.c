#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int		i;
	int		len_s;

	if (!s || !f)
		return ;
	i = 0;
	len_s = ft_strlen(s);
	while (i < len_s)
		f(&s[i++]);
}
