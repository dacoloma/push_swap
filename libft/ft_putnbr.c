#include "libft.h"

void	ft_putnbr(int n)
{
	long long lln;

	lln = n;
	if (lln < 0)
	{
		ft_putchar('-');
		lln = -lln;
	}
	if (lln >= 10)
		ft_putnbr(lln / 10);
	ft_putchar(lln % 10 + '0');
}
